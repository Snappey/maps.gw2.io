// Stamps the current build time into src/environments/build-info.ts so the
// About dialog can show when the site was last updated. The file is gitignored
// and regenerated automatically before every build (`prebuild`) and after
// install (`postinstall`), so dev/test always have a copy and each production
// build carries a fresh date.
//   node scripts/generate-build-info.mjs

import {writeFileSync} from "node:fs";
import {fileURLToPath} from "node:url";
import {dirname, join} from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const outFile = join(here, "..", "src", "environments", "build-info.ts");

// SOURCE_DATE_EPOCH (seconds) lets a reproducible-build pipeline pin the stamp;
// otherwise use the current instant. Either way it is regenerated on build.
const epoch = process.env.SOURCE_DATE_EPOCH;
const buildDate = (epoch ? new Date(Number(epoch) * 1000) : new Date()).toISOString();

const contents = `// AUTO-GENERATED by scripts/generate-build-info.mjs — do not edit by hand.
// Gitignored; regenerated on every build (prebuild) and install (postinstall).
export const buildInfo = {
  // ISO-8601 instant this build was produced.
  buildDate: "${buildDate}",
} as const;
`;

writeFileSync(outFile, contents);
console.log(`[build-info] stamped ${buildDate}`);
